// Slicing And Extracting Elements
let name = "John";
let age = 30;
console.log(name.slice(0, 4)); // Output: "John"

// Extracting Elements
console.log(name[0]); // Output: "J"
console.log(name.charAt(0)); // Output: "J"

// Extracting Substrings
console.log(name.substring(0, 4)); // Output: "John"

//Casing text
console.log(name.toUpperCase()); // Output: "JOHN"
console.log(name.toLowerCase()); // Output: "john"

// Splitting Strings
console.log(name.split(" ")); // Output: ["John"]
console.log(name.split("")); // Output: ["J", "o", "h", "n"]